/* Operadores relacionales o de comparacion
     Mayor que: a > b               a es mayor que b
     Menor que: a < b               a es menor que b
     Mayor o igual que: a >= b      a es mayor o igual a b
     Menor o igual que: a <= b      a es menor o igual a b
     Igual: a == b                  a es igual a b
     Distinto: a != b               a es diferente de b
     Operador estricto: ===         a es estrictamente igual a b
     Operador diferente estricto: !== a es estrictamente diferente de b
*/
let numero1 = 22;
let numero2 = 15;

console.log(`¿Número 1: ${numero1} > número 2: ${numero2}? :  ${numero1 > numero2} `);
console.log(`¿Número 1: ${numero1} < número 2: ${numero2}? :  ${numero1 < numero2}`);
console.log(`¿Número 1: ${numero1} >= número 2: ${numero2}? :  ${numero1 >= numero2}`);
console.log(`¿Número 1: ${numero1} <= número 2: ${numero2}? :  ${numero1 <= numero2}`);
console.log(`¿Número 1: ${numero1} == número 2: ${numero2}? :  ${numero1 == numero2}`);
console.log(numero1 != numero2);
console.log(numero1 === numero2);
console.log(numero1 !== numero2);